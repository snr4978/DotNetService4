namespace Kean.Infrastructure.Database
{
    /// <summary>
    /// 聚合函数
    /// </summary>
    public static class Function
    {
        /// <summary>
        /// 平均值
        /// </summary>
        /// <param name="column">字段</param>
        public static object Avg(object column) => default;

        /// <summary>
        /// 计数
        /// </summary>
        /// <param name="column">字段</param>
        public static object Count(object column) => default;

        /// <summary>
        /// 最大值
        /// </summary>
        /// <param name="column">字段</param>
        public static object Max(object column) => default;

        /// <summary>
        /// 最小值
        /// </summary>
        /// <param name="column">字段</param>
        public static object Min(object column) => default;

        /// <summary>
        /// 求和
        /// </summary>
        /// <param name="column">字段</param>
        public static object Sum(object column) => default;
    }
}
